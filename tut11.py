# Operators in Python

# Arithmetic Operators
print("Arithmetic Operators -")
print("24 + 4 = ",24+4)
print("24 - 4 = ",24-4)
print("24 * 4 = ",24*4)
print("24 / 4 = ",24/4)
print("24 // 4 = ",24//4) # Floor Division ignores after decimal point
print("24 ** 4 = ",24**4)
print("24 % 4 = ",24%4)

# Assignment Operators
print("Assignment Operators -")
n = 6
print(n)
n -= 1
print(n)
n += 2
print(n)
n *= 2
print(n)
n /= 2
print(n)

# Comparison Operators
print("Comparison Operators -")
print(5 == 5)
print(5 > 6)
print(5 < 100)
print(5 <= 5)
print(5 >= 19)

# Logical Operators
print("Logical Operators -")
x = True
y = False
print(x and y)
print(x or y)

# Identity Operators
print("Identity Operators -")
print(24 is 5)
print(24 is not 5)

# Membership Operators
print("Membership Operators -")
l = [45,24,325,24,64]
print(46 not in l)
print(46 in l)
print(24 in l)

# Bitwise Operators
print("Bitwise Operators")
print(0 & 1)
print(0 | 1)